a = "名字是：{0}, 年龄是：{1}"
b = a.format("zxq", 22)
print(b)
c = a.format("钟锡权", 22)
print(c)

d = "名字是：{0}, 年龄是：{1},{0}是个好小伙。"
e = d.format("钟锡权", 22)
print(e)

f = "名字是：{name}, 年龄是：{age}"
g = f.format(age=22, name="钟锡权")
print(g)


# 填充与对齐
"""
^、<、> 分别是居中，左对齐，右对齐，后面带宽度
 ：后面填充的字符，只能是一个字符，不指定的话默认是空格填充。
"""

h = "{:*>8}".format("zxq")
print(h)

i = "我是{0}，我喜欢数字{1:*^9}".format("钟锡权", 666)
print(i)

# 数字格式化
"""浮点数通过f，整数通过d进行需要的格式化"""

j = "我是{0},我的存款有{1:.2f}".format("钟锡权", 3999.2321312)
print(j)

